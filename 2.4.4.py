# Входные данные: С клавиатуры вводится три строки.
# Выходные данные: Выводится три строки.
# Sample Input 1: ау            Sample Output 1: кто
#                 ты                             ты
#                 кто                            ау 
####################################################################################################

text_1 = input()
text_2 = input()
text_3 = input()
