########################################################

a, b, c = input(), input(), input()
print(c, b, a, sep='\n')    # sep- контролирует то, какой разделитель будет использоваться между элементами. По умолчанию используется пробел:

########################################################

a, b, c = [input() for i in range(3)]
print(c,b,a, sep = '\n')

########################################################

print(*reversed([input() for _ in range(3)]), sep='\n') # 'reversed' - переворот строки с обратной итерацией

#######################################################

print(*[input(),input(),input()][::-1], sep='\n') # срез последовательности с [::-1] создает перевернутую копию

